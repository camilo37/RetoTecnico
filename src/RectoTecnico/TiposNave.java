    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        
        if(rdbLanzadera.isSelected()==true){

            OpcLanzadora verOpcLanzadora = new OpcLanzadora();
            verOpcLanzadora.setVisible(true);
            this.dispose();
        }else if(rdbNoTripulada.isSelected()==true){

            OpcNoTripuladas verOpcNoTripuladas = new OpcNoTripuladas();
            verOpcNoTripuladas.setVisible(true);
            this.dispose();
        }else if(rdbTripulada.isSelected()==true){

            OpcTripuladas verOpcTripuladas = new OpcTripuladas();
            verOpcTripuladas.setVisible(true);
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(null,"Celecione un campo");
        }
        
        
    }//GEN-LAST:event_btnCrearActionPerformed

